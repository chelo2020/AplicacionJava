    public void LimpiarTable(){
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i=i-1;   
        }
    }
    public void ListarCliente() {
        List<Cliente> ListarCl = (List<Cliente>) client.ListarCliente();
        modelo = (DefaultTableModel) jTable1.getModel();
        Object[] ob = new Object[7];
        for (int i = 0; i < ListarCl.size(); i++) {
            ob[0] = ListarCl.get(i).getId_cliente();
            ob[1] = ListarCl.get(i).getNombre_cliente();
            ob[2] = ListarCl.get(i).getApellido_cliente();
            ob[3] = ListarCl.get(i).getDni_cliente();
            ob[4] = ListarCl.get(i).getEdad_cliente();
            ob[5] = ListarCl.get(i).getCorreo_cliente();
            modelo.addRow(ob);
        }
        jTable1.setModel(modelo);

    }
    public void ValidarCliente() {
        String nombre=jTextField1.getText();
         if (!"".equals(nombre) ) {
            Cliente cl2 = (Cliente) client.Buscarcliente2(nombre);
            if (cl2.getNombre_cliente()!= null) {
               List<Cliente> ListarCl = (List<Cliente>) client.Buscarcliente2(nombre);
               modelo = (DefaultTableModel) jTable1.getModel();
               Object[] ob = new Object[7];
               for (int i = 0; i < ListarCl.size(); i++) {
                    ob[0] = ListarCl.get(i).getId_cliente();
                    ob[1] = ListarCl.get(i).getNombre_cliente();
                    ob[2] = ListarCl.get(i).getApellido_cliente();
                    ob[3] = ListarCl.get(i).getDni_cliente();
                    ob[4] = ListarCl.get(i).getEdad_cliente();
                    ob[5] = ListarCl.get(i).getCorreo_cliente();
                    modelo.addRow(ob);
                }
                jTable1.setModel(modelo);
            }else{
                JOptionPane.showMessageDialog(null, "El cliente no existe");
            }
        }
        

    }

